// Function name: explainRepl
function explainRepl() {
    console.log("REPL stands for:");
    console.log("1. READ: Reads the user's input.");
    console.log("2. EVAL: Evaluates the JavaScript expression.");
    console.log("3. PRINT: Prints the result.");
    console.log("4. LOOP: Loops back to the read step to wait for more input.");
    
    console.log("\nTo test, type 'node' in your CLI to enter the environment.");
    console.log("Example: You can type '5 + 5' and it will output 10 immediately.");
}

explainRepl();